#!/usr/bin/env python3
"""
This module contains a type-annotated function 'to_str'.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The float number to convert.

    Returns:
        str: The string representation of n.
    """
    return str(n)
